int main()
{
    int n, k1, f, s;
    scanf("%d %d", &n, &k1);
   while(k1!=0)
    {
        scanf("%d %d", &f, &s);
        create_node(f,s);
        k1--;
    }

    int *b=(int *)malloc(n*sizeof(int));
    int p=0;
    for(p=0;p<n;p++)
    {
        b[p]=p;
    }

    permute(b,0,n-1);
    printf("%d", count);
}


