#include<stdio.h>
#include<stdlib.h>
#include<math.h>

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int count = 0;
struct node
{
    int f, s;
    struct node *next;

};

struct node *beg=NULL, *end=NULL, *i= NULL, *j= NULL, *k=NULL;
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void create_node(int f, int s)
{

    struct node *temp=(struct node*)malloc(sizeof(struct node));
    temp->f=f;
    temp->s=s;
    temp->next=NULL; //EXTREMELY IMPORTANT, DONT FORGET

    if(beg==NULL)
    {
        beg=temp;
        end=temp;
    }

    else{
        end->next=temp;
        end=temp;
    }

}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void transit()
{ int c;
    for(i=beg;i!=NULL;i=i->next)
        for(j=beg;j!=NULL;j=j->next)
    {
        if(i->s==j->f)
        {   c=0;
            for(k=beg;k!=NULL;k=k->next)
            {
                if(i->f==k->f&&j->s==k->s)
                    c++;
            }

            if(c==0)
                create_node(i->f,j->s);
        }
    }
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int check(int *a, int n)
{
    int f, s, z=0;
    for(i=beg;i!=NULL;i=i->next)
    {
        for(f=0;f<n;f++)
        {
            if(a[f]==i->f)
                break;
        }

        for(s=0;s<n;s++)
        {
            if(a[s]==i->s)
                break;
        }

        if(f>s)
        {
            z=1;
            break;
        }


    }

    if(z==1)
        return 0;
    else
        return 1;

}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void swap(int *a, int *b)
{
    int temp=*a;
    *a=*b;
    *b=temp;
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void permute(int *a, int l, int r)
{   int p=0;
    if(l==r)
    {

        if(check(a,r+1)==1)
            count++;

    }

    else{
    for(p=l;p<=r;p++)
    {
        swap(a+p,a+l);
        permute(a, l+1,r);
        swap(a+p,a+l);
    }
    }
}
